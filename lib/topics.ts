// 기준일 (이 날짜의 주제가 배열의 0번째)
const EPOCH = new Date(2024, 0, 1);

// 날짜를 기준으로 그날의 주제를 고름 (같은 날엔 항상 같은 주제, 자정이 지나면 다음 주제, 다 쓰면 처음으로 순환)
export function getTodayTopic(date: Date = new Date()): Topic {
  const daysSinceEpoch = Math.floor(
    (Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()) -
      Date.UTC(EPOCH.getFullYear(), EPOCH.getMonth(), EPOCH.getDate())) /
      (1000 * 60 * 60 * 24)
  );

  const index =
    ((daysSinceEpoch % TOPICS.length) + TOPICS.length) % TOPICS.length;

  return TOPICS[index];
}

// 주제에 등장하는 카테고리 목록 (중복 없이, 등장 순서대로)
export const CATEGORIES: string[] = Array.from(
  new Set(TOPICS.map((topic) => topic.category))
);

// category를 지정하면 그 카테고리 안에서만, 지정하지 않거나 '전체'면 전체 주제 중에서 랜덤으로 하나를 고름
export function getRandomTopic(category?: string): Topic {
  const pool =
    category && category !== "전체"
      ? TOPICS.filter((topic) => topic.category === category)
      : TOPICS;

  const index = Math.floor(Math.random() * pool.length);
  return pool[index];
}
