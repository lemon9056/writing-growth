// 기준일 (이 날짜의 주제가 배열의 0번째)
const EPOCH = new Date(2024, 0, 1);

// 날짜를 기준으로 그날의 주제를 고름 (같은 날엔 항상 같은 주제, 자정이 지나면 다음 주제, 30개를 다 쓰면 처음으로 순환)
export function getTodayTopic(date: Date = new Date()): string {
  const daysSinceEpoch = Math.floor(
    (Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()) -
      Date.UTC(EPOCH.getFullYear(), EPOCH.getMonth(), EPOCH.getDate())) /
      (1000 * 60 * 60 * 24)
  );

  const index =
    ((daysSinceEpoch % TOPICS.length) + TOPICS.length) % TOPICS.length;

  return TOPICS[index];
}
